const yaml = require('js-yaml');
const fs = require('fs');
const json = fs.readFileSync("../schemas/swagger-v3.v3.json", "utf-8")
const content = JSON.parse(json)
const output = yaml.dump(content, { noRefs: true });
const banner = '### GENERATED by gen/genCI.ts!\n\n';
const fullContent = banner + output;
console.log(fullContent);
fs.writeFileSync("../schemas/swagger-v3.v3.yaml", fullContent, 'utf8');